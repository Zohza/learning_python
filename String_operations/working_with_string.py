# Converts all the characters into uppercase using upper()
name = "Ada Balogun"
print(name.upper())

# Using lower() to convert string to lowercase
sentence = "python is amazing"
print(sentence.title())

# Removing whitespaces using strip()
text = "   Abuja"
print(text.strip())

# Replacing occurences of substring with another substring using replace()
message = "I love Java"
print(message.replace("Java", "Python"))

# switching lowercase to uppercase using swapcase()
text = "Hello ABEOKUTA"
print(text.swapcase())

# removing whitespace or characters from the leftside only
text = "    Nigeria"
print(text.lstrip())

